package common.item;

public class ItemVO {

	private int seq;
	private String code_name;
	private String code;
	private String upper_code;
	private String code_cn;
	private String use_at;
	
	
	public int getSeq() {
		return seq;
	}
	public void setSeq(int seq) {
		this.seq = seq;
	}
	public String getCode_name() {
		return code_name;
	}
	public void setCode_name(String code_name) {
		this.code_name = code_name;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getUpper_code() {
		return upper_code;
	}
	public void setUpper_code(String upper_code) {
		this.upper_code = upper_code;
	}
	public String getCode_cn() {
		return code_cn;
	}
	public void setCode_cn(String code_cn) {
		this.code_cn = code_cn;
	}
	public String getUse_at() {
		return use_at;
	}
	public void setUse_at(String use_at) {
		this.use_at = use_at;
	}
	
	
	
}
